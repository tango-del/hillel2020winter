package Examples;

public class Calculate {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int mult(int a, int b) {
        return a * b;
    }

    public static int sub(int a, int b) {
        return a - b;
    }

    public static double div(int a, int b) {
        return a / b;
    }

    public static boolean tex(int a, int b) {
        return a < b;
    }
}
